#include<stdio.h>
int main ()
{
    int a;
    char b;
    float c;

    a = 12; //12 is given as input directly
    b = 'A'; //To give the character variable b a value directly, you should assign a character within single quotes ''. 
    c= 3.14;

    printf("The integer value is = %d \n",a);
    printf("The character is = %c \n",b); // Printing the character
    printf("The float value is = %f \n",c); // Printing the float value

    return 0;
}